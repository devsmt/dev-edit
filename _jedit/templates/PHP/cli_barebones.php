class Main {
    public static function run($argv) {
        $action = isset($argv[1]) ? $argv[1] : 'test';

        switch($action) {
            case 'x':
                die(' ... ');
            break;
            default:
                die(self::actionUsage());
            break;
        }
    }

    //----------------------------------------------------------------------------
    //  actions
    //----------------------------------------------------------------------------
    function actionUsage() {
            return <<<__END__
    uso:
        {$argv[0]} [action] [--go] [--test]
    uso del programma
__END__;
    }
}

//----------------------------------------------------------------------------
//  main
//----------------------------------------------------------------------------
Main::run($argv);


